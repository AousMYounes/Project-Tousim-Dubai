 Gran Tour con Desayuno Atmosférico: Finaliza tu Gran Tour con un delicioso desayuno en Atmospheric, el restaurante ubicado en el piso 122 del Burj Khalifa. Los recorridos duran 60 minutos; El desayuno dura hasta 1,5 horas. El precio es de 450 AED por adulto y 250 AED por niño de entre 3 y 16 años.</p>
